package com.demo.crawlerapi.Entity;

public class Fixture {
    private String fixtureDateTime;
    private String fixtureInfo;
    private String homeTeam;
    private String awayTeam;
    private String fixtureJson;

    public String getFixtureDateTime() {
        return fixtureDateTime;
    }

    public void setFixtureDateTime(String fixtureDateTime) {
        this.fixtureDateTime = fixtureDateTime;
    }

    public String getFixtureInfo() {
        return fixtureInfo;
    }

    public void setFixtureInfo(String fixtureInfo) {
        this.fixtureInfo = fixtureInfo;
    }

    public String getHomeTeam() {
        return homeTeam;
    }

    public void setHomeTeam(String homeTeam) {
        this.homeTeam = homeTeam;
    }

    public String getAwayTeam() {
        return awayTeam;
    }

    public void setAwayTeam(String awayTeam) {
        this.awayTeam = awayTeam;
    }

    public String getFixtureJson() {
        return fixtureJson;
    }

    public void setFixtureJson(String fixtureJson) {
        this.fixtureJson = fixtureJson;
    }
}
